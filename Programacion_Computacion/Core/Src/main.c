		// Lectura de las aceleraciones
		HAL_I2C_Mem_Read(&hi2c1,(0x68<<1 |0x01),0x3B,1,(uint8_t *)aceleracion,6,HAL_MAX_DELAY); // Leo las 6 aceleraciones
