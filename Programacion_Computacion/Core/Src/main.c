		aceleracion_Y=aceleracion[2]<<8 | aceleracion[3];
		aceleracion_Z=aceleracion[4]<<8 | aceleracion[5];
		
		// Calculo las aceleraciones en m/s^2
		aceleracion_X=(((float)aceleracion_X*2.0)/32767.0)*9.80665; // Convierto la aceleracion a m/s^2
